##Antecessor e Sucessor / Predecessor and Successor / Predecesor și Succesor

n = int(input('Digite um numero:'))
a = n - 1
s = n + 1
print(f'Analisando o valor {n}, seu antecessor é {a} e seu sucessor é {s}')
print("Analisando o valor {}, seu antecessor é {} e seu sucessor é {}".format(n,a,s))
#ou
n1 = int(input('Digite um numero:'))
print ('Analisando o valor {}, seu antecessor é {} e o seu sucessor é {}'.format(n1,(n1-1),(n1+1)))
print(f'Analisando o valor de {n1}, seu antecessor é {n1-1} e o seu sucessor é {n1+1}')
